package cake;

public class Cake {

    //////////// Fields ////////////////
    protected String flavour;
    protected double price;


    ////////////// Getters and Setters ///////////
    public String getFlavour() {
        return flavour;
    }

    public void setFlavour(String flavour) {
        this.flavour = flavour;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
